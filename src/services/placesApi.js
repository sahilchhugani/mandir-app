// Simulate network delay
const delay = (ms) => new Promise(resolve => setTimeout(resolve, ms));

/**
 * Search for temples
 * Uses backend when available, falls back to mock data
 */
export async function searchTemples(query = '', location = null) {
  console.log('ðŸ” Searching temples...', { query, location });
  
  // Try backend first
  try {
    const params = new URLSearchParams();
    if (query) params.set('query', query);
    if (location) {
      params.set('lat', location.lat);
      params.set('lng', location.lng);
    }
    
    const response = await fetch(`${BACKEND_URL}/api/temples?${params}`, {
      signal: AbortSignal.timeout(5000) // 5 second timeout
    });
    
    if (response.ok) {
      const data = await response.json();
      console.log('âœ… Got temples from backend:', data.length);
      return data;
    }
  } catch (err) {
    console.log('âš ï¸ Backend unavailable, using mock data');
  }
  
  // Fall back to mock data
  await delay(600);
  
  let results = [...mockTemplesData];
  
  // Filter by query
  if (query) {
    const q = query.toLowerCase();
    results = results.filter(t => 
      t.name.toLowerCase().includes(q) ||
      t.location.toLowerCase().includes(q) ||
      t.deity.toLowerCase().includes(q) ||
      t.state.toLowerCase().includes(q) ||
      (t.hindiName && t.hindiName.includes(q))
    );
  }
  
  console.log(`âœ… Found ${results.length} temples (mock data)`);
  
  return results.map(t => ({
    ...t,
    isWishlisted: false,
    isVisited: false,
    friendRatings: [],
  }));
}

/**
 * Get details for a specific temple
 */
export async function getTempleDetails(templeId) {
  console.log('ðŸ“ Getting temple details...', { templeId });
  
  // Try backend first
  try {
    const response = await fetch(`${BACKEND_URL}/api/temples/${templeId}`, {
      signal: AbortSignal.timeout(5000)
    });
    
    if (response.ok) {
      return await response.json();
    }
  } catch (err) {
    console.log('âš ï¸ Backend unavailable, using mock data');
  }
  
  // Fall back to mock data
  await delay(400);
  
  const temple = mockTemplesData.find(t => t.id === templeId || t.id === parseInt(templeId));
  if (!temple) throw new Error('Temple not found');
  
  return {
    ...temple,
    isWishlisted: false,
    isVisited: false,
    friendRatings: [],
  };
}

